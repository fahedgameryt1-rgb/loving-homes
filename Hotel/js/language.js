function setText(el, value){ if(el && value !== undefined) el.textContent = value; }
function setHTML(el, value){ if(el && value !== undefined) el.innerHTML = value; }
function setTextAll(selector, values){ document.querySelectorAll(selector).forEach((el,i)=>{ if(values[i]!==undefined) el.textContent = values[i]; }); }
function setAttr(el, attr, value){ if(el && value !== undefined) el.setAttribute(attr, value); }
function setOptionTexts(selector, values){ const el = document.querySelector(selector); if(!el) return; [...el.options].forEach((opt,i)=>{ if(values[i]!==undefined) opt.textContent = values[i]; }); }

function pageKey(){
  const p = window.location.pathname.toLowerCase();
  if(p.endsWith('/services.html')) return 'services';
  if(p.endsWith('/packages.html')) return 'packages';
  if(p.endsWith('/tips.html')) return 'tips';
  if(p.endsWith('/about.html')) return 'about';
  if(p.endsWith('/contact.html')) return 'contact';
  if(p.endsWith('/reservation.html')) return 'reservation';
  return 'index';
}

